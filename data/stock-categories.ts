// Function to get stocks by category
export function getStocksByCategory(categoryId: string) {
  return stocksWithCategories.filter((stock) => stock.category === categoryId)
}

// Function to get a stock by symbol
export function getStockBySymbol(symbol: string) {
  return stocksWithCategories.find((stock) => stock.symbol === symbol)
}

// Function to get a category by ID
export function getCategoryById(categoryId: string) {
  return stockCategories.find((category) => category.id === categoryId)
}
